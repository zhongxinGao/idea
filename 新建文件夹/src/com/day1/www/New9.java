package com.day1.www;

public class New9 {
    public static void main(String[] args) {
        //字符转数字
        char a ='a';
        char b = 'b';
        System.out.println(a);
        System.out.println(a+0);
        System.out.println((int)a);

        System.out.println(b);
        System.out.println(b+0);
        System.out.println((int)b);
    }
}
